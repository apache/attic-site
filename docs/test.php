<div class="section-content">
<p>
    The Apache Attic was created in 
    <a href="http://www.apache.org/foundation/records/minutes/2008/board_minutes_2008_11_19.txt">November 2008</a> 
    to provide process and solutions to make it clear when an Apache project has reached its 
    end of life. Specifically to be:  
  </p>
<p style="font-family: monospace">
    "responsible for the oversight of projects which otherwise would not have 
    oversight; and be it further ... is not authorized to actively develop and release the 
    projects under its oversight"
  </p>
<p>It is intended to: </p>
<ul>
    <li>Be non-impacting to users</li>
    <li>Provide restricted oversight for these codebases</li>
    <li>Provide oversight for active user lists with no Project Management Committee (PMC)</li>
  </ul>
<p>It is not intended to: </p>
<ul>
    <li>Rebuild community</li>
    <li>Make bugfixes</li>
    <li>Make releases</li>
  </ul>
</div>
                 
<h4 id="incoming">
   Getting into the Attic
</h4>
<div class="section-content">
<p>There are two expected mechanisms by which a project may enter the Attic. Either the managing 
     Project Management Committee (PMC) decides it would like to move the project, or The 
     Apache Software Foundation's board dissolves the PMC and chooses to move the project. </p>
<p>In either case, the following may be useful: <a href="resolution.html">Template Board Resolution</a>. </p>
</div>
                 
<h4 id="candidates">
   When should a project move to the Attic?
</h4>
<div class="section-content">
<p>Projects whose PMC are unable to muster 3 votes for a release, who have no active committers or 
     are unable to fulfill their reporting duties to the board are all good candidates for the Attic. </p>
</div>
                 
<h4 id="process">
   Process of moving into the Attic
</h4>
<div class="section-content">
<p>This is defined in the separate <a href="process.html">process</a> document. </p>
</div>
                 
<h4 id="departure">
   Process of leaving the Attic again
</h4>
<div class="section-content">
<p>Options are: </p>
<ul>
    <li>Forking the project - we'll link to any forks which have been created so please let us know</li>
    <li>Restarting the community in the Apache Incubator</li>
    <li>Recreating a PMC for the project</li>
  </ul>
</div>
                 
<h4 id="contact">
   Contacting the Attic PMC
</h4>
<div class="section-content">
<p>The Attic is managed by a Project Management Committee (PMC). Discussions are found on 
     the <a href="http://mail-archives.apache.org/mod_mbox/attic-general/">general@ mailing list</a>
     and you are welcome to subscribe. </p>
</div>
                 
<h4 id="incubator">
   Retired Incubator Projects
</h4>
<div class="section-content">
<p>The Apache Incubator project is the entry path into The Apache Software Foundation for projects and codebases wishing to become part of the Foundation's efforts. Not all projects 'graduate' out of the Incubator and are instead retired. Those retired projects may be found on the Incubator's <a href="http://incubator.apache.org/projects/index.html#retired">Project page</a>. </p>
</div>
